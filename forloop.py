fruits = ["apple", "banana", "cherry"]
for x in fruits:
  print(x)

#looping through a string
for x in "banana":
    print(x)

#looping through a range
for x in range(6):
    print(x)

#range with starting value
for x in range(2, 6):
    print(x)

#range with increment
for x in range(0, 10, 2):
    print(x)

